    public void validar(){
    String dni=txtPass.getText();
    String user = txtUser.getText();
    if(txtUser.getText().equals("")||txtPass.getText().equals("")){
        JOptionPane.showMessageDialog(this, "No deje espacios en blanco.");
    txtUser.requestFocus();
    }else{
        ev=v.ValidarVendedor(dni, user);
        if (ev.getUser()!= null && ev.getDni() != null){
        Principal p = new Principal();
        p.setVisible(true);
        dispose();
       
        }else{
        JOptionPane.showMessageDialog(this, "Ponga valores validos");
        txtUser.requestFocus();
        }
    }
}
