    void agregarProducto() {
        double total;
        //int item=0;
        modelo = (DefaultTableModel) TablaDetalle.getModel();
        item = item + 1;
        idProducto = p.getId();
        String nomp = txtProducto.getText();
        pre = Double.parseDouble(txtPrecio.getText());
        cant = Integer.parseInt(cantidad.getValue().toString());
        int stock = Integer.parseInt(txtStock.getText());
        total = cant * pre;
        ArrayList lista = new ArrayList();

        if (stock > 0) {
            lista.add(item);
            lista.add(idProducto);
            lista.add(nomp);
            lista.add(cant);
            lista.add(pre);
            lista.add(total);
            Object[] ob = new Object[6];
            ob[0] = lista.get(0);
            ob[1] = lista.get(1);
            ob[2] = lista.get(2);
            ob[3] = lista.get(3);
            ob[4] = lista.get(4);
            ob[5] = lista.get(5);

            modelo.addRow(ob);
            TablaDetalle.setModel(modelo);
            calcularTotal();

        } else {
            JOptionPane.showMessageDialog(this, "Stock no disponible");
        }
    }

    void calcularTotal() {
        tot = 0;
        for (int i = 0; i < TablaDetalle.getRowCount(); i++) {
            cant = Integer.parseInt(TablaDetalle.getValueAt(i, 3).toString());
            pre = Double.parseDouble(TablaDetalle.getValueAt(i, 4).toString());
            tot = tot + (cant * pre);
        }
        txtTotalPagar.setText("" + tot);
    }

    void buscarProducto() {
        int id = Integer.parseInt(txtCodProducto.getText());

        if (txtCodProducto.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el codigo del producto");
        } else {
            p = pcrud.listarID(id);

            if (p.getId() != 0) {
                txtProducto.setText(p.getNom());
                txtPrecio.setText("" + p.getPre());
                txtStock.setText("" + p.getStock());
            } else {
                JOptionPane.showMessageDialog(this, "Producto no registrado ");
                txtCodProducto.requestFocus();
            }
        }
    }

