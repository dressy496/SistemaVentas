    void agregarTablaProducto() {
        if (txtNombreProducto.getText().isEmpty() || txtPrecioProducto.getText().isEmpty() || txtStockProducto.getText().isEmpty() || comboProducto.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
            return;
        }

        try {
            p.setNom(txtNombreProducto.getText());
            p.setPre(Double.parseDouble(txtPrecioProducto.getText()));
            p.setStock(Integer.parseInt(txtStockProducto.getText()));
            p.setEstado(comboProducto.getSelectedItem().toString());

            pcrud.RegistrarProducto(p);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Verifica los campos numéricos (Precio y Stock).");
        }

    }

